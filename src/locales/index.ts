// 🟢 手动读取本地存储的语言设置
const getUserLocale = () => {
  const saved = localStorage.getItem('app-settings');
  if (saved) {
    try {
      return JSON.parse(saved).language || 'zh';
    } catch (e) {
      return 'zh';
    }
  }
  return 'zh';
};

